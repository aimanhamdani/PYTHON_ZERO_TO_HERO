#String slicing
w='Welcome to Wscubetech'
print(len(w))
print(w[0::2]) #W,l,o,e,t, ,s,u,e,e,h
print(w[0::3])
#reversing
print(w[-1::-2])
print('-------')
print(w[-1::-1])
print('------------------------------------------')
